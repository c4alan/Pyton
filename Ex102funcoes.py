def soma(a,b):
    soma = int(a+b)
    print(F"A soma de A e B é {soma}")

def subtracao(a,b):
    sub = int(a - b)
    print(f"A subtração de A e B é {sub}")

def multi(a,b):
    multi = int(a * b)
    print(f"A multiplicação de A e B é {multi}")

def divi(a,b):
    divi = int(a / b)
    print(f"A divisão de A e B é {divi}")

